    public ImageIcon resizeImage(String path, JLabel label) {
        ImageIcon icon1 = new ImageIcon(path);
        Image image = icon1.getImage();
        Image newImage = image.getScaledInstance(label.getWidth(), label.getHeight(),
                Image.SCALE_SMOOTH);

        ImageIcon newResized = new ImageIcon(newImage);
        return newResized;
    }

    public void computerLogic() {
        //Random rand = new Random();
        computer_choice = (int) (Math.random() * 3);
        switch (computer_choice) {
            case 0:
                computerIcon.setIcon(resizeImage("src/Game_Images/rockImg.jpg", computerIcon));
                System.out.println("computer choose rock");
                break;
            case 1:
                computerIcon.setIcon(resizeImage("src/Game_Images/paperImg.jpg", computerIcon));
                System.out.println("computer choose paper");
                break;
            case 2:
                computerIcon.setIcon(resizeImage("src/Game_Images/scissorsImg.jpg", computerIcon));
                System.out.println("computer choose scissors");
                break;

        }
    }

    public void checkWinner() {
        if (human_choice == computer_choice) {
            gameStatus.setText("TIE");
            winnerlbl.setText("TIE!");
        } // scenerio for player beating computer
        else if (human_choice == 0 && computer_choice == 2) {// rock beats scissors
            gameStatus.setText("Player wins");
            winnerlbl.setText("Rock beats Scissors");
            playerScore++;
            playerlblscore.setText(Integer.toString(playerScore));
        } else if (human_choice == 1 && computer_choice == 0) {// paper beats rock
            gameStatus.setText("Player wins");
            winnerlbl.setText("Paper beats Rock");
            playerScore++;
            playerlblscore.setText(Integer.toString(playerScore));
        } else if (human_choice == 2 && computer_choice == 1) {// scissors beats paper
            gameStatus.setText("Player wins");
            winnerlbl.setText("Scissors beats Paper");
            playerScore++;
            playerlblscore.setText(Integer.toString(playerScore));
        } //scenerio for computer beating player
        else if (computer_choice == 0 && human_choice == 2) {// rock beats scissors
            gameStatus.setText("Computer wins");
            winnerlbl.setText("Rock beats Scissors");
            computerScore++;
            complblscore.setText(Integer.toString(computerScore));
        } else if (computer_choice == 1 && human_choice == 0) {// paper beats rock
            gameStatus.setText("Computer wins");
            winnerlbl.setText("Paper beats Rock");
            computerScore++;
            complblscore.setText(Integer.toString(computerScore));
        } else if (computer_choice == 2 && human_choice == 1) {// scissors beats rock
            gameStatus.setText("Computer wins");
            winnerlbl.setText("Scissors beats Paper");
            computerScore++;
            complblscore.setText(Integer.toString(computerScore));
        }

    }

