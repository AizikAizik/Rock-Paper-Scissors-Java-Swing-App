    private void resetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetBtnActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(this, "Are you sure u want to reset Game!!", "Prompt", JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        //if user clicks yes
         if (res == JOptionPane.YES_OPTION){
        int resetScore = 0;
        // Set both score back to Zero
        playerlblscore.setText(Integer.toString(resetScore));
        complblscore.setText(Integer.toString(resetScore));

        //set both scores back to zero
        playerScore = 0;
        computerScore = 0;

        // set both icons back to null
        userIcon.setIcon(null);
        computerIcon.setIcon(null);

        // set game status label to empty or null
        gameStatus.setText("");

        //set Winner label status to empty or null
        winnerlbl.setText("");
         }
    }//GEN-LAST:event_resetBtnActionPerformed

